# Create a tuple of favorite colors
favorite_colors = ("blue", "green", "red", "yellow", "purple")

# a. Access the first and last elements of the tuple
first_color = favorite_colors[0]
last_color = favorite_colors[-1]
print("First color:", first_color)
print("Last color:", last_color)

# b. Concatenate two tuples together
another_colors = ("orange", "pink")
concatenated_tuple = favorite_colors + another_colors
print("Concatenated tuple:", concatenated_tuple)

# c. Repeat the tuple three times and display the result
repeated_tuple = favorite_colors * 3
print("Repeated tuple three times:", repeated_tuple)
