import math

# Square root of a number
sqrt_result = math.sqrt(16)
print("Square root of 16:", sqrt_result)  # Output: 4.0

# Factorial of a number
factorial_result = math.factorial(5)
print("Factorial of 5:", factorial_result)  # Output: 120

# Value of sin(90°)
sin_result = math.sin(math.radians(90))
print("sin(90°):", sin_result)  # Output: 1.0
