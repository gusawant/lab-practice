import numpy as np

# Create two matrices
A = np.array([[1, 2], [3, 4]])
B = np.array([[5, 6], [7, 8]])

# Matrix Operations
sum_matrix = A + B
difference_matrix = A - B
product_matrix = A * B
division_matrix = A / B

print("Sum of matrices:\n", sum_matrix)
print("Difference of matrices:\n", difference_matrix)
print("Product of matrices:\n", product_matrix)
print("Division of matrices:\n", division_matrix)
